class GradeEntity {
  final double grade;
  final double fullDegree;
  final int id;
  final String subjectName;
  final int subjectId;
  final int semester;
  final int gradeYear;

  GradeEntity({
    required this.grade,
    required this.fullDegree,
    required this.id,
    required this.subjectName,
    required this.subjectId,
    required this.semester,
    required this.gradeYear
  });
}
